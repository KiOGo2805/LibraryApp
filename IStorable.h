#pragma once

/*
 * Цей файл оголошує інтерфейс IStorable.
 * IStorable - це абстрактний базовий клас для всіх об'єктів,
 * які можна зберігати у базі даних (наприклад, у класі Library).
 */

#include <string> // Для використання std::string

 /**
  * @class IStorable
  * @brief Абстрактний базовий клас (інтерфейс) для об'єктів,
  * що можуть зберігатися.
  *
  * Визначає загальний контракт, який повинні реалізувати всі класи-нащадки,
  * наприклад, надавати унікальний ідентифікатор та назву типу.
  */
class IStorable
{
public:
    /**
     * @brief Віртуальний деструктор.
     *
     * Забезпечує коректне викликання деструкторів похідних класів
     * при видаленні об'єктів через вказівник на базовий клас.
     */
    virtual ~IStorable() {}

    /**
     * @brief Отримує унікальний ідентифікатор об'єкта (артикул).
     * @return Рядок std::string, що містить ID.
     */
    virtual std::string GetId() const = 0;

    /**
     * @brief Отримує назву типу об'єкта.
     * @return Рядок std::string (наприклад, "Book").
     */
    virtual std::string GetTypeName() const = 0;

    // Згідно з Addendum A (1.3), далі йдуть protected та private
protected:
    // Немає захищених членів

private:
    // Немає приватних членів
};